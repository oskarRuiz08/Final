    private void botonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCrearActionPerformed
        String dataCantidad = JOptionPane.showInputDialog(null, "dame el tamaÃ±o del arreglo", "pilas mano", JOptionPane.INFORMATION_MESSAGE);
        if (dataCantidad != null) {
            try {
                int i, cantidad;
                cantidad = Integer.parseInt(dataCantidad);
                miArreglito = new MiArreglo(cantidad);
                miModelito.clear();
                for (i = 0; i < cantidad; i++) {
                    String dataValor = JOptionPane.showInputDialog(null, "ingrese un numero", "pilas mano", JOptionPane.INFORMATION_MESSAGE);
                    if (dataValor == null) {
                        miModelito.addElement("le diste cancelar ");
                        i = cantidad;
                    } else {
                        try {
                            int elValor = Integer.parseInt(dataValor);
                            miArreglito.agregarElemento(i, elValor);
                        } catch (Exception miError) {
                            i--;
                            JOptionPane.showMessageDialog(null, "dijite unicamente caracteres numericos ", "ADVERTENCIa", JOptionPane.WARNING_MESSAGE);
                        }
                    }
                }
            } catch (Exception error) {
                JOptionPane.showMessageDialog(null, "solo dijite caracteres numericos ", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_botonCrearActionPerformed

